	<section>
		<h2>§ 1 Geltungsbereich und Anbieter</h2>
		<p>
			(1) Diese Allgemeinen Geschäftsbedingungen („AGB“) gelten für die Nutzung der Plattform
			dyad.berlin (der „Dienst“), für den Abschluss von Mitgliedschaften sowie für den Kauf
			des Zines über den Dienst.
		</p>
		<p>
			(2) Anbieter ist die Monad Engineering UG (haftungsbeschränkt), Richard-Sorge-Straße 24,
			10249 Berlin („wir“). Kontakt:
			<a href="mailto:legal@dyad.berlin">legal@dyad.berlin</a>.
		</p>
		<p>
			(3) Unser Angebot richtet sich ausschließlich an Verbraucherinnen und Verbraucher im
			Sinne des § 13 BGB mit gewöhnlichem Aufenthalt in Deutschland, die mindestens 18 Jahre
			alt sind.
		</p>
		<p>(4) Entgegenstehende oder abweichende Bedingungen werden nicht Vertragsbestandteil.</p>
	</section>

	<section>
		<h2>§ 2 Der Dienst</h2>
		<p>
			(1) dyad hilft Menschen, sich für persönliche Gespräche zu treffen: Mitglieder
			veröffentlichen Gesprächsanlässe („Konversationen“), antworten darauf, laden einander
			ein und verabreden sich zu Treffen.
		</p>
		<p>
			(2) Wir stellen die technische Plattform zur Verfügung. Wir sind weder Veranstalter der
			Treffen noch Vertragspartei etwaiger Abreden zwischen Mitgliedern.
		</p>
		<p>
			(3) Der Dienst befindet sich im Aufbau (Beta). Funktionsumfang und Gestaltung können
			sich weiterentwickeln; der Kernbestand der Mitgliedsfunktionen (§ 4 Abs. 2) bleibt
			davon unberührt.
		</p>
	</section>

	<section>
		<h2>§ 3 Registrierung und Konto</h2>
		<p>
			(1) Die Nutzung setzt ein Konto voraus. Die Aufnahme erfolgt derzeit über Einladungen
			und eine Warteliste; ein Anspruch auf Aufnahme besteht nicht.
		</p>
		<p>
			(2) Angaben bei der Registrierung müssen wahrheitsgemäß sein. Jede Person darf nur ein
			Konto führen. Konten sind nicht übertragbar.
		</p>
		<p>
			(3) Zugangsdaten sind geheim zu halten. Über eine missbräuchliche Nutzung des Kontos
			sind wir unverzüglich zu informieren.
		</p>
	</section>

	<section>
		<h2>§ 4 Kostenlose Nutzung und Mitgliedschaft</h2>
		<p>(1) Die Nutzung des Dienstes ist in Grundfunktionen kostenlos.</p>
		<p>
			(2) Bestimmte Funktionen — insbesondere das Starten von Konversationen, das Antworten
			und die Verabredung zu Treffen — sind ganz oder teilweise einer entgeltlichen
			Mitgliedschaft vorbehalten. Welcher Funktionsumfang zur Mitgliedschaft gehört, ergibt
			sich aus der Darstellung im Dienst zum Zeitpunkt des Abschlusses.
		</p>
	</section>

	<section>
		<h2>§ 5 Preise und Zahlung</h2>
		<p>
			(1) Die Mitgliedschaft ist als monatliche oder jährliche Mitgliedschaft oder gegen
			Einmalzahlung („Lifetime“) erhältlich. Es gelten die im Dienst zum Zeitpunkt der
			Bestellung angezeigten Preise; alle Preise verstehen sich inklusive Umsatzsteuer.
		</p>
		<p>
			(2) Für die monatliche Mitgliedschaft stehen mehrere Beitragsstufen zur Wahl (Solidar-,
			Standard- und Förderbeitrag). Der Leistungsumfang ist bei allen Stufen identisch; die
			Stufen ermöglichen einen selbst gewählten Beitrag.
		</p>
		<p>
			(3) Die Zahlungsabwicklung erfolgt über den Zahlungsdienstleister Stripe. Zur Zahlung
			werden Sie auf eine von Stripe betriebene Seite weitergeleitet. Wir speichern keine
			Zahlungsdaten (näher: <a href="/datenschutz">Datenschutzerklärung</a>).
		</p>
		<p>(4) Der Vertrag über die Mitgliedschaft kommt mit Abschluss des Bezahlvorgangs zustande.</p>
	</section>

	<section>
		<h2>§ 6 Laufzeit und Kündigung</h2>
		<p>
			(1) Die monatliche bzw. jährliche Mitgliedschaft verlängert sich jeweils um einen Monat
			bzw. ein Jahr, wenn sie nicht bis zum Ende des laufenden Abrechnungszeitraums gekündigt
			wird.
		</p>
		<p>
			(2) Die Kündigung ist jederzeit möglich: über die Mitgliedschaftsverwaltung im Profil
			oder in Textform an <a href="mailto:legal@dyad.berlin">legal@dyad.berlin</a>. Sie wirkt
			zum Ende des laufenden Abrechnungszeitraums; bis dahin bleiben die Mitgliedsfunktionen
			nutzbar.
		</p>
		<p>
			(3) Die Lifetime-Mitgliedschaft wird durch Einmalzahlung erworben und gilt für die
			Dauer des Bestehens des Dienstes. Sie begründet keinen Anspruch auf einen unbegrenzten
			Fortbestand des Dienstes; § 13 und zwingende gesetzliche Rechte bleiben unberührt.
		</p>
		<p>
			(4) Das Recht zur außerordentlichen Kündigung aus wichtigem Grund bleibt für beide
			Seiten unberührt.
		</p>
		<p>
			(5) Endet die Mitgliedschaft, bleibt das Konto bestehen; mitgliedschaftspflichtige
			Funktionen sind pausiert. Bereits erstellte Inhalte bleiben erhalten.
		</p>
	</section>

	<section>
		<h2>§ 7 Kauf des Zines</h2>
		<p>
			(1) Über die Seite <a href="/zine">/zine</a> bieten wir ein gedrucktes Heft („Zine“)
			zum Kauf an. Es gelten die dort angezeigten Preise, teils in mehreren selbst wählbaren
			Preisstufen bei identischer Ware.
		</p>
		<p>(2) Der Versand erfolgt aus Berlin, in der Regel innerhalb von 5–7 Tagen nach Bestellung.</p>
		<p>(3) Kauf und Zahlung werden über Stripe abgewickelt; § 5 Abs. 3 gilt entsprechend.</p>
	</section>

	<section>
		<h2>§ 8 Verhaltensregeln</h2>
		<p>
			(1) Mitglieder begegnen einander mit Respekt. Untersagt sind insbesondere: Belästigung,
			Diskriminierung, Nötigung, Missbrauch, Doxing und andere Verletzungen der Privatsphäre,
			nicht einvernehmliches Verhalten, Spam, Identitätstäuschung, missbräuchliche Nutzung
			der Meldefunktion sowie unsicheres Verhalten im Zusammenhang mit Treffen.
		</p>
		<p>
			(2) Unsere Community-Richtlinien (<a href="/community-care">dyad.berlin/community-care</a>)
			konkretisieren diese Regeln in ihrer jeweils veröffentlichten Fassung.
		</p>
		<p>
			(3) Eingestellte Inhalte dürfen keine Rechte Dritter verletzen und nicht gegen
			geltendes Recht verstoßen.
		</p>
	</section>

	<section>
		<h2>§ 9 Moderation</h2>
		<p>
			(1) Auf Verstöße können wir mit verhältnismäßigen Maßnahmen reagieren: Hinweis oder
			Verwarnung, Entfernen von Inhalten, vorübergehende Einschränkung von Funktionen,
			Sperrung oder — in schweren oder wiederholten Fällen — Ausschluss vom Dienst.
		</p>
		<p>
			(2) Betroffene werden über Maßnahme und Grund informiert und können der Entscheidung
			widersprechen (<a href="mailto:legal@dyad.berlin">legal@dyad.berlin</a>). Der
			Widerspruch wird nach Möglichkeit von einer anderen Person geprüft als der, die
			entschieden hat.
		</p>
	</section>

	<section>
		<h2>§ 10 Inhalte der Mitglieder</h2>
		<p>
			(1) Rechte an Inhalten, die Mitglieder einstellen (z.&nbsp;B. Konversationen,
			Antworten, Bilder), verbleiben bei ihnen.
		</p>
		<p>
			(2) Mitglieder räumen uns das einfache, unentgeltliche Recht ein, diese Inhalte im
			Rahmen des Dienstes zu speichern, technisch zu verarbeiten und den vorgesehenen
			Empfängern anzuzeigen. Das Recht endet mit der Löschung des Inhalts bzw. des Kontos,
			soweit keine gesetzlichen Aufbewahrungspflichten bestehen.
		</p>
	</section>

	<section>
		<h2>§ 11 Treffen und Eigenverantwortung</h2>
		<p>
			(1) Treffen zwischen Mitgliedern finden in eigener Verantwortung statt. Wir überprüfen
			die Identität von Mitgliedern nicht umfassend und können Angaben anderer Mitglieder
			nicht garantieren.
		</p>
		<p>
			(2) Wir empfehlen, Treffen an öffentlichen Orten zu verabreden. Der genaue Treffpunkt
			wird erst nach beidseitiger Bestätigung angezeigt. Probleme im Zusammenhang mit einem
			Treffen können über die Meldefunktion gemeldet werden.
		</p>
	</section>

	<section>
		<h2>§ 12 Verfügbarkeit</h2>
		<p>
			Wir bemühen uns um eine hohe Verfügbarkeit des Dienstes, schulden aber keine
			ununterbrochene Erreichbarkeit. Wartung, Weiterentwicklung und Störungen können zu
			vorübergehenden Einschränkungen führen.
		</p>
	</section>

	<section>
		<h2>§ 13 Haftung</h2>
		<p>
			(1) Wir haften unbeschränkt für Vorsatz und grobe Fahrlässigkeit, für Schäden aus der
			Verletzung des Lebens, des Körpers oder der Gesundheit sowie nach dem
			Produkthaftungsgesetz.
		</p>
		<p>
			(2) Bei einfach fahrlässiger Verletzung wesentlicher Vertragspflichten (Pflichten,
			deren Erfüllung die ordnungsgemäße Durchführung des Vertrags überhaupt erst ermöglicht
			und auf deren Einhaltung regelmäßig vertraut werden darf) ist die Haftung auf den
			vertragstypischen, vorhersehbaren Schaden begrenzt. Im Übrigen ist die Haftung für
			einfache Fahrlässigkeit ausgeschlossen.
		</p>
		<p>
			(3) Für das Verhalten anderer Mitglieder — online wie bei Treffen — haften wir nicht;
			Absatz 1 und 2 bleiben unberührt.
		</p>
	</section>

	<section>
		<h2>§ 14 Änderungen dieser AGB</h2>
		<p>
			(1) Wir können diese AGB mit Wirkung für die Zukunft ändern, soweit dies aus triftigen
			Gründen erforderlich ist (z.&nbsp;B. Gesetzesänderungen, neue Funktionen) und
			Mitglieder dadurch nicht unangemessen benachteiligt werden.
		</p>
		<p>
			(2) Änderungen kündigen wir mindestens sechs Wochen vor Wirksamwerden in Textform an.
			Widerspricht ein Mitglied nicht bis zum Wirksamwerden, gelten die Änderungen als
			angenommen; auf diese Folge weisen wir in der Ankündigung gesondert hin. Im Fall des
			Widerspruchs kann jede Seite das Vertragsverhältnis zum Zeitpunkt des Wirksamwerdens
			der Änderung kündigen.
		</p>
	</section>

